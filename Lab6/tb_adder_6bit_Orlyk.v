    // ?????? ???????
    reg [5:0] Ain_r, Bin_r;
    reg Ci_r;

    // ?????? ???? ???????
    wire [5:0] s_struct, s_ref;
    wire co_struct, co_ref;

    // ???????????? ??????????? ??????
    adder_6bit_struct uut_struct (Ain_r, Bin_r, Ci_r, s_struct, co_struct);
    
    // ???????????? ????????? ??????
