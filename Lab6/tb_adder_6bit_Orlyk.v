    initial begin
        $display("Time\t Ain\t Bin\t Ci | Struct_S Co | Ref_S Co | Match");
        $monitor("%0t\t %d\t %d\t %b  | %d\t %b  | %d\t %b  | %b", 
                 $time, Ain_r, Bin_r, Ci_r, s_struct, co_struct, s_ref, co_ref, 
                 (s_struct == s_ref && co_struct == co_ref));

        // ????????? ??????
        Ci_r = 0; Ain_r = 10; Bin_r = 5;  #10; // 10 + 5 = 15
        Ci_r = 0; Ain_r = 32; Bin_r = 31; #10; // 32 + 31 = 63 (???? ??? ???????????)
        Ci_r = 1; Ain_r = 60; Bin_r = 3;  #10; // 60 + 3 + 1 = 64 (????????????)
        Ci_r = 0; Ain_r = 0;  Bin_r = 0;  #10;
        
        // ????????? ????
        repeat(5) begin
            Ain_r = $random; Bin_r = $random; Ci_r = $random; #10;
        end

        $stop;
    end
endmodule
