module Control_Dev_Orlyk (
    input wire x,
    output reg [2:0] mi // ? [Y1 Y2 Y3]
);

    // ????????? ??????
    parameter a1 = 2'b00, a2 = 2'b01, a3 = 2'b10;
    reg [1:0] curr_state;
    reg clk, reset;

    // ????????? ????????? clk ?? reset (??? ?????????)
    initial begin
        clk = 0;
        reset = 1;
        #15 reset = 0; // ???????? ????? 15 ??
    end
    always #10 clk = ~clk; // ?????? 20 ??

    // ?????? ????????? ?? ?????????? ???????
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            curr_state <= a1;
            mi <= 3'b010; // ???? a1: ??????? y2
        end else begin
            case (curr_state)
                a1: begin
                    curr_state <= a2;
                    mi <= 3'b100; // ???? a2: ??????? y1
                end
                a2: begin
                    curr_state <= a3;
                    mi <= 3'b011; // ???? a3: ??????? y2, y3
                end
                a3: begin
                    if (x) begin
                        curr_state <= a2;
                        mi <= 3'b100; // ?????????? ?? a2
                    end else begin
                        curr_state <= a1;
                        mi <= 3'b010; // ?????????? ?? a1
                    end
                end
                default: curr_state <= a1;
            endcase
        end
    end
endmodule

